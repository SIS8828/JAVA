public class ControlStatement_FOR_08 {

	public static void main(String[] args) {
		for (int i = 1 ; i <= 10 ; i++) {
			//특정 반복문의 실행 코드들이 조건에 맞는 경우만 
			// 실행되는 경우, 반대의 조건식을 사용하여 다음 반복순서로
			// 이동하게 만든코드
			// 홀스인 경우 다음을 반복
			if ( i % 2 == 1)
				continue ;
			// 짝수의 값만 출력
				System.out.printf("i = %d\n", i);
				System.out.printf("i = %d\n", i);
				System.out.printf("i = %d\n", i);
				System.out.printf("i = %d\n", i);
				System.out.printf("i = %d\n", i);
				System.out.printf("i = %d\n", i);
				System.out.printf("i = %d\n", i);
				System.out.printf("i = %d\n", i);
				System.out.printf("i = %d\n", i);
				
			}
	}
}
