
public class Variable_02 {

	public static void main(String[] args) {
		//변수의 다양한 선언 방법
		
		// 1. 변수의 선언 후, 값을 할당
		int n1;
		n1 = 10;
		
		// 2. 변수의 선언과 값을 대입을 동시에 처리
		int n2 = 20;
		
		//3. 다수개의 변수를 선언하는 방법
		// 동일한 자료형만 가능
		// 자료형 변수명, 변수명2 ...;
		int n3, n4, n5;
		
		// 4. 다수개의 변수를 값을 대입하면서 생성하는 방법
		int n6 = 60, n7 = 70;
		int n8, n9 = 90, n10 ;
		
	}

}
