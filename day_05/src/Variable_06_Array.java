		// 1. 자료형 [] 배열변수명;
		// 2. 자료형 배열변수명[];
		
		// 크기가 3인 배열을 생성하는 코드
		int [] arr1 = new int[3];
		int arr2 [] = new int[3];
		
		// 배열 변수 arr3, arr4를 선언하는 코드
		int [] arr3, arr4;
		arr3 = new int[3];
		arr4 = new int[3];
		// 배열 변수 arr5, arr6, 
		// 일반 int형 변수 arr7을 생성하는 코드
