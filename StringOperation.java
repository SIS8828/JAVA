
public class StringOperation {

	public static void main(String[] args) {

		// 자바 언어에서는 + 연산자를 사용할 ㅅ ㅜ있습니다.
		// + 연산자를 수치 계산에 사용하는 예제
		System.out.println(10 + 5);
		
		// + : 좌항과 우항을 결합하여 하나의 문자열로반환
		System.out.println("Hello " + "World~!");
		System.out.println(10 + " + " + 5 +" = " + 10 + 5 );
		System.out.println(10 + " + " + 5 +" = " + (10 + 5) );

		//
	}

}
